    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        if(txtTitulo.getText().equals("") 
                || txtEditora.getText().equals("") 
                || txtAnoLancamento.getText().equals("") 
                || tblAutores.getSelectedRowCount() < 1){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!!");
        }else{
            
            //CADASTRA
            
            String titulo = txtTitulo.getText();
            int anoLancamento = Integer.parseInt(txtAnoLancamento.getText());
            String editora = txtEditora.getText();
            int aut = tblAutores.getSelectedRow();
                    
                   
            if ( aut >= 0 ) {
                autSelecionado = (Autor) tblAutores.getValueAt(aut, 1);
                
                
                if ( ( (JButton) evt.getSource() ).getMnemonic() == 'N'  ) {
                    // Inserir no BANCO DE DADOS
                    int id = 0;
                try {
                    id = controladorIHC.getControlDominio().inserirLivro(titulo, anoLancamento, editora, autSelecionado);
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarLivros.class.getName()).log(Level.SEVERE, null, ex);
                }

                    JOptionPane.showMessageDialog(this, "Livro " + id + " inserido com sucesso.");
                } else {
                try {
                    // ALTERAR NO BANCO DE DADOS
                    controladorIHC.getControlDominio().alterarLivro(livSelecionado.getIdLivro(), titulo, anoLancamento, editora, autSelecionado);
                } catch (SQLException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                }
                

                    JOptionPane.showMessageDialog(this, "Livro " + livSelecionado.getIdLivro()+ " alterado com sucesso.");
                }
                
            limparCampos();
        }
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(txtTitulo.getText().equals("")
                                            && txtAnoLancamento.getText().equals("")
                                            && txtEditora.getText().equals("")
                                            && (tblAutores.getSelectedRowCount() == 0)){
            
            if(JOptionPane.showConfirmDialog(null, "Deseja sair?") == JOptionPane.YES_OPTION ){
                this.dispose();
            }    
        }
        
        limparCampos();
        
        
        btnNovo.setEnabled(true);
    }//GEN-LAST:event_btnCancelarActionPerformed

