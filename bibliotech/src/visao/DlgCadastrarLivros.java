    private void limparCampos(){
        
        txtTitulo.setText("");
        txtAnoLancamento.setText("");
        txtEditora.setText("");
        tblAutores.getSelectionModel().clearSelection();
    }
    
    
    private void preencherCampos(Livro liv) {
        if ( liv != null ) {
            txtTitulo.setText( liv.getTitulo() );
            txtAnoLancamento.setText(String.valueOf(liv.getAnoLancamento()));
            txtEditora.setText( liv.getEditora());
            int temp = retornaLinhaAutor(liv.getAutor().getIdAutor());
            tblAutores.setRowSelectionInterval(temp, temp);
        }
        
    }
    
    
    private int retornaLinhaAutor(int id){
        int cont = 0;
        
        for(cont = 0; cont < tblAutores.getRowCount(); cont++){
            tblAutores.setRowSelectionInterval(cont, cont);
            if(id == Integer.parseInt( tblAutores.getValueAt(cont, 0).toString()))
            {
                return cont;
            }
        }
        
        return -1;
    }
    

