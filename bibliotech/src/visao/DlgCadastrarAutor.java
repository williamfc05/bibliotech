    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        if(txtNome.getText().equals("") 
                || txtAnoNasc.getText().equals("") 
                || txtNota.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!!");
        }else{
            
            //CADASTRA
            
            String nome = txtNome.getText();
            int anoNasc = Integer.parseInt(txtAnoNasc.getText());
            String nota = txtNota.getText();


                if ( ( (JButton) evt.getSource() ).getMnemonic() == 'N'  ) {
                    // Inserir no BANCO DE DADOS
                    
                
                    int id = 0;
                try {
                    id = controladorIHC.getControlDominio().inserirAutor(nome, anoNasc, nota);
                } catch (SQLException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                   JOptionPane.showMessageDialog(this, "Autor " + id + " inserido com sucesso.");
                } else {
                try {
                    // ALTERAR NO BANCO DE DADOS
                    controladorIHC.getControlDominio().alterarAutor(autSelecionado.getIdAutor(), nome, anoNasc, nota);
                } catch (SQLException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarAutor.class.getName()).log(Level.SEVERE, null, ex);
                }
                

                    JOptionPane.showMessageDialog(this, "Autor " + autSelecionado.getIdAutor() + " alterado com sucesso.");
                }
                
            limparCampos();
        }
    }//GEN-LAST:event_btnNovoActionPerformed

