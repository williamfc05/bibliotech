    private void limparCampos(){
        
        txtNome.setText("");
        txtAnoNasc.setText("");
        txtNota.setText("");
    }
    
    
    
    private void preencherCampos(Autor aut) {
        if ( aut != null ) {
            txtNome.setText( aut.getNome() );
            txtAnoNasc.setText(String.valueOf(aut.getAnoNascimento()));
            txtNota.setText( aut.getNotaBio());
              
        }
        
    }
    
