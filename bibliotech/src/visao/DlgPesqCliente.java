    private void btnPesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqActionPerformed
        // APAGAR A TABELA
        ((DefaultTableModel) tblClientes.getModel()).setRowCount(0);

        try {
            controladorIHC.getControlDominio().pesquisarCliente(txtNome.getText(), txtCpf.getText(), tblClientes);
        } catch (SQLException ex) {
            Logger.getLogger(DlgPesqCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPesqActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linha = tblClientes.getSelectedRow();
        
        if ( linha >= 0 ) {
            cliSelecionado = (Cliente) tblClientes.getValueAt(linha, 0);
            setVisible(false);
        } else {
            cliSelecionado = null;
            JOptionPane.showMessageDialog(this, "Selecione um cliente.", "Erro Cliente", JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tblClientes.getSelectedRow();

        if (linha >= 0) {
            Cliente cli = (Cliente) tblClientes.getValueAt(linha, 0);
            try {
                controladorIHC.getControlDominio().excluirObj(cli);
                ((DefaultTableModel) tblClientes.getModel()).removeRow(linha);
                JOptionPane.showMessageDialog(this, "Cliente exclu√≠do com sucesso.");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir cliente.", "Erro Cliente", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente.", "Erro Cliente", JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

