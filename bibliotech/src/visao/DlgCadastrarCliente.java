    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(txtNome.getText().equals("")
                                            && txtDtNasc.getText().equals("")
                                            && txtEndereco.getText().equals("")
                                            && txtEmail.getText().equals("")
                                            && txtTelefone.getText().equals("")){
            
            if(JOptionPane.showConfirmDialog(null, "Deseja sair?") == JOptionPane.YES_OPTION ){
                this.dispose();
            }    
        }
        
        limparCampos();
        
        
        btnNovo.setEnabled(true);
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        if(txtNome.getText().isEmpty() || txtCpf.getText().isEmpty()
                                       || txtDtNasc.getText().isEmpty() 
                                       || txtEndereco.getText().isEmpty() 
                                       || txtEmail.getText().isEmpty()
                                       || txtTelefone.getText().isEmpty() 
                                       || !radioBtnFeminino.isSelected()
                                       && !radioBtnMasculino.isSelected()){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!!");
        }else{
            
            //CADASTRA
            
            String nome = txtNome.getText();
            String cpf = txtCpf.getText();
            String dtNasc = this.txtDtNasc.getText();
            char sexo = (char) buttonGroup1.getSelection().getMnemonic();
            String tel = txtTelefone.getText();
            String endereco = txtEndereco.getText();
            String email = txtEmail.getText();

      

   

            

                if ( ( (JButton) evt.getSource() ).getMnemonic() == 'N'  ) {
                    // Inserir no BANCO DE DADOS
                    int id = 0;
                try {
                    id = controladorIHC.getControlDominio().inserirCliente(cpf, dtNasc, email, endereco, nome, sexo, tel);
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarCliente.class.getName()).log(Level.SEVERE, null, ex);
                }

                    JOptionPane.showMessageDialog(this, "Cliente " + id + " inserido com sucesso.");
                } else {
                try {
                    controladorIHC.getControlDominio().alterarCliente(cliSelecionado.getIdCliente(), cpf, dtNasc, email, endereco, nome, sexo, tel);
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(DlgCadastrarCliente.class.getName()).log(Level.SEVERE, null, ex);
                }

                    JOptionPane.showMessageDialog(this, "Cliente " + cliSelecionado.getIdCliente() + " alterado com sucesso.");
                }
       
                limparCampos();
        } 
    }//GEN-LAST:event_btnNovoActionPerformed

